#Bitwise operators
a =10
b =4
#bitwise AND operator
print("a & b= ",a&b)
#bitwise OR operator
print("a | b = ",a|b)
#bitwise NOT operator
print("~a =",~a)
#bitwise XOR operator
print("a ^ b = ",a ^ b)

#shift operator
a =10
