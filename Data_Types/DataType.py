# tuple
x =("Ravi","Raju","Raja")
print(type(x))

#range
x = range(10)
print(type(x))

#dict
x ={"Name": "MAnoj","Age": 19}
print(type(x))

#set
x ={"Ravi","Raju","Raja"}
print(type(x))

#frozenset
