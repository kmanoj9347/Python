# string slicing

# creating a string
String1 = "GoodMorning"
print("Initial String : ")
print(String1)

#printing 4th to 9th character
print("\nSlicing character from 4-9: ")
print(String1[4:9])

#printing character between 
# 4th and 3rd character
print("\nSlicing character between " +
      "3rd and 3rd last character: ")
print(String1[4:-3])